package org.iita.trainingunit.applications.service.impl;

public class CDOApplicationException extends Exception {

	/**
	 * Serial version ID
	 */
	private static final long serialVersionUID = 4876653997283690372L;

	public CDOApplicationException() {

	}

	public CDOApplicationException(String message) {
		super(message);
	}

	public CDOApplicationException(Throwable cause) {
		super(cause);
	}

	public CDOApplicationException(String message, Throwable cause) {
		super(message, cause);
	}
}

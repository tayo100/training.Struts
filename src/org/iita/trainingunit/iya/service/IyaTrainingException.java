package org.iita.trainingunit.iya.service;

/**
 * @author ken
 *
 */
public class IyaTrainingException extends Exception{
	/**
	 * Serial version ID
	 */
	private static final long serialVersionUID = 4876653997283690372L;

	public IyaTrainingException() {

	}

	public IyaTrainingException(String message) {
		super(message);
	}

	public IyaTrainingException(Throwable cause) {
		super(cause);
	}

	public IyaTrainingException(String message, Throwable cause) {
		super(message, cause);
	}
}
